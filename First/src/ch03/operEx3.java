package ch03;

public class operEx3 {

	public static void main(String[] args) {
		
		int father = 45;
		int mother = 46;
		char ch;
		ch = (father > mother ) ? 'T' : 'F';
		System.out.println("나이가 많나?");
		System.out.println(ch);
		
		
		int num = 0;
		num += 2;
		System.out.println(num);
		num -= 2; 
		System.out.println(num);
		num = 10;
		num *= 10;
		System.out.println(num);
		
	}

}
